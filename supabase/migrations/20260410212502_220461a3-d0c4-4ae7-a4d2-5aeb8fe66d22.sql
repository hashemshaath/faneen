
-- Create contract_templates table
CREATE TABLE public.contract_templates (
  id UUID NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
  category TEXT NOT NULL DEFAULT 'general',
  name_ar TEXT NOT NULL,
  name_en TEXT,
  description_ar TEXT,
  description_en TEXT,
  terms_ar TEXT NOT NULL,
  terms_en TEXT,
  scope_of_work_ar TEXT,
  scope_of_work_en TEXT,
  warranty_terms_ar TEXT,
  warranty_terms_en TEXT,
  payment_terms_ar TEXT,
  payment_terms_en TEXT,
  penalties_ar TEXT,
  penalties_en TEXT,
  notes_ar TEXT,
  notes_en TEXT,
  is_active BOOLEAN NOT NULL DEFAULT true,
  sort_order INTEGER NOT NULL DEFAULT 0,
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),
  updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now()
);

ALTER TABLE public.contract_templates ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Contract templates are viewable by authenticated users"
  ON public.contract_templates FOR SELECT
  TO authenticated USING (true);

CREATE POLICY "Admins can manage contract templates"
  ON public.contract_templates FOR ALL
  TO authenticated
  USING (public.has_role(auth.uid(), 'admin'))
  WITH CHECK (public.has_role(auth.uid(), 'admin'));

